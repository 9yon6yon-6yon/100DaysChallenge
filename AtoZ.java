public class AtoZ {
    public static void main(String[] args){
        for (char c='A';c<='Z';c++){//same for small latters
           
            System.out.println(c);
        }
        System.out.println("-----------------");
        for (char c='a';c<='z';c++){           
            System.out.println(c);
        }
    }
}
